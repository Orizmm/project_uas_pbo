    public void showTableData(){
        Connection.koneksi();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama Fakultas");
        String sql = "SELECT * from fakultas";
        
        try{
            items.clear();
            Connection.rs = Connection.stmt.executeQuery(sql);
            while(Connection.rs.next()){
                items.add(new Fakultas(Connection.rs.getInt("id_fakultas"), Connection.rs.getString("nama_fakultas")));
            }
            int i = 1;
            for(Fakultas f: items){
                model.addRow(new Object[]{
                    i,f.getNamaFakultas()
                });
                i++;
            }
            Connection.rs.close();
            tblFakultas.setModel(model);
        }catch(Exception e){
            e.printStackTrace();
        }
    }
