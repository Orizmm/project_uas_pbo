    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String nama = txtNamaFak.getText();
        String sql = "INSERT INTO fakultas (nama_fakultas) VALUES (?)";
        try{
            PreparedStatement ps = Connection.conn.prepareStatement(sql);
            ps.setString(1, nama);
            
            ps.execute();
            Connection.stmt.close();
            Connection.conn.close();
        }catch(Exception e){
            e.printStackTrace();
        }
        showTableData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String nama = txtNamaFak.getText();
        int id = Integer.parseInt(txtid.getText());
        String sql = "UPDATE fakultas SET nama_fakultas=? WHERE id_fakultas=?";
        try{
            PreparedStatement ps = Connection.conn.prepareStatement(sql);
            ps.setString(1, nama);
            ps.setInt(2, id);
            ps.execute();
            
            Connection.stmt.close();
            Connection.conn.close();
        }catch(Exception e){
            e.printStackTrace();
        }
        showTableData();       
    }//GEN-LAST:event_btnEditActionPerformed

