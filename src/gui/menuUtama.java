    public int getPenIndex(List<MahasiswaBaru> authors, int id)
    {
        int targetIndex = -1;
        
        for(int i = 0; i < authors.size(); i++)
        {
            if(authors.get(i).getId_pendaftaran()== id)
            {
                targetIndex = i;
                break;
            }
        }
        
       return targetIndex;
    }
